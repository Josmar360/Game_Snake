package com.mukeshsolanki.snake.presentation.theme

import android.graphics.Color.rgb
import androidx.compose.ui.graphics.Color
/*La línea de código val LightGreen = Color(rgb(148,0,211)) crea una instancia de la clase
Color utilizando la función rgb para especificar los componentes de color rojo, verde y azul
(RGB) del color deseado. En este caso, se utiliza el valor (148, 0, 211) para representar un tono de verde claro.*/
val LightGreen = Color(rgb(148,0,211))

/*La línea de código val DarkGreen = Color(rgb(0, 255, 255)) crea una instancia de la clase Color utilizando
la función rgb para especificar los componentes de color rojo, verde y azul (RGB) del color deseado. En este
caso, se utiliza el valor (0, 255, 255) para representar un tono de verde oscuro.*/
val DarkGreen = Color(rgb(0,255,255))